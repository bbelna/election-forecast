/**
 * Margins required for each rating level.
 */
export interface RatingMargins {
  /**
   * Margin required for a solid rating.
   */
  solid: number;

  /**
   * Margin required for a likely rating.
   */
  likely: number;

  /**
   * Margin required for a lean rating.
   */
  lean: number;

  /**
   * Margin required for a tilt rating.
   */
  tilt: number;
}
